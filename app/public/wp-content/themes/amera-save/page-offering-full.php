                tooltips[i].tip.parentNode.removeChild(tooltips[i].tip);
              }
                tooltips = [];
            };
            var remove_tooltip = function(elem) {
              for (var i = 0; i < tooltips.length; i++) {
                if (tooltips[i].elem === elem) {
                  tooltips[i].tip.parentNode.removeChild(tooltips[i].tip);
                  tooltips.splice(i, 1);
                  return;
                }
              }
