                  if (elem.tagName.toLowerCase() !== "select") {
                    elem.value = elem.value.trim();
                  }
                  validate_field(elem) ? true : no_error = false;
                }
              }
              if (!no_error && e) {
                e.preventDefault();
              }
              resize_tooltips();
              return no_error;
