                        }
                      })(input);
                    } else if (input.tagName == 'SELECT') {
                      addEvent(input, 'change', function() {
                        validate_field(this, true);
                      });
                    } else if (input.type == 'textarea'){
                      addEvent(input, 'input', function() {
                        validate_field(this, true);
