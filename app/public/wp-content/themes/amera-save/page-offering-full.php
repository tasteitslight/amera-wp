                  else {
                    no_error = false;
                    for (var i = 0; i < elems.length; i++) {
                      if (elems[i].checked) no_error = true;
                    }
                  }
                  if (!no_error) {
                    tooltip = create_tooltip(elem, "Please select an option.");
                  }
                } else if (elem.type =='checkbox') {
